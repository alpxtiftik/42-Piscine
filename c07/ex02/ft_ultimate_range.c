#include <stdlib.h>

int	ft_ultimate_range(int **range, int min, int max)
{
	int	i;

	if (min >= max)
	{
		*range = NULL;
		return (0);
	}
	*range = (int *) malloc((sizeof(int) * (max - min)));
	if (*range == NULL)
		return (-1);
	i = 0;
	while (min < max)
	{
		(*range)[i] = min;
		min++;
		i++;
	}
	return (i);
}
int main ()
{
	int *range;
	printf("%d", ft_ultimate_range(&range, -5, 10));
	printf("%d", range[0]);
}